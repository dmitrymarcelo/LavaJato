const deploySha = process.env.DEPLOY_SHA || process.env.GITHUB_SHA;

if (!deploySha) {
  throw new Error('DEPLOY_SHA ou GITHUB_SHA deve estar definido para gerar o payload do deploy.');
}

const deployScript = [
  '#!/usr/bin/env bash',
  'set -euo pipefail',
  'DEBUG_LOG="/opt/lavajato/runtime/DEPLOY_DEBUG.txt"',
  'mkdir -p /opt/lavajato/runtime',
  'exec > >(tee "${DEBUG_LOG}") 2>&1',
  'publish_debug_log() {',
  '  if docker compose ps --services 2>/dev/null | grep -qx "web"; then',
  '    docker compose exec -T web sh -lc \'cat > /usr/share/nginx/html/deploy-debug.txt\' < "${DEBUG_LOG}" || true',
  '  fi',
  '}',
  'log_failure() {',
  '  echo "Deploy falhou. Estado atual dos containers:"',
  '  docker compose ps || true',
  '  echo "Uso atual de disco do Docker:"',
  '  docker system df || true',
  '  echo "Logs recentes do web:"',
  '  docker compose logs --tail 120 web || true',
  '  echo "Logs recentes da api:"',
  '  docker compose logs --tail 120 api || true',
  '  publish_debug_log',
  '}',
  'trap log_failure ERR',
  `DEPLOY_SHA="${deploySha}"`,
  'cd /opt/lavajato/app',
  'git checkout -- HANDOFF.md AGENTS.md SKILLS.md || true',
  'git fetch origin',
  'git checkout main',
  'git pull --ff-only origin main',
  'CURRENT_SHA="$(git rev-parse HEAD)"',
  'echo "Repository SHA: ${CURRENT_SHA}"',
  'if [ "${CURRENT_SHA}" != "${DEPLOY_SHA}" ]; then',
  '  echo "Repository SHA mismatch. Expected ${DEPLOY_SHA}, got ${CURRENT_SHA}."',
  '  exit 1',
  'fi',
  'TOKEN="$(curl -fsS -X PUT \'http://169.254.169.254/latest/api/token\' -H \'X-aws-ec2-metadata-token-ttl-seconds: 21600\')"',
  'PUBLIC_IP="$(curl -fsSH "X-aws-ec2-metadata-token: ${TOKEN}" http://169.254.169.254/latest/meta-data/public-ipv4)"',
  'APP_HOST="${PUBLIC_IP}"',
  'APP_CERT_NAME="${PUBLIC_IP}"',
  'HTTPS_CERT_EMAIL="${HTTPS_CERT_EMAIL:-}"',
  'echo "Canonical HTTPS host: ${APP_HOST}"',
  'CERTBOT_CONTACT_ARGS=()',
  'if printf "%s" "${HTTPS_CERT_EMAIL}" | grep -Eq "^[^[:space:]@]+@[^[:space:]@]+\\.[^[:space:]@]+$"; then',
  '  CERTBOT_CONTACT_ARGS=(--email "${HTTPS_CERT_EMAIL}")',
  '  echo "Using certbot contact email: ${HTTPS_CERT_EMAIL}"',
  'else',
  '  CERTBOT_CONTACT_ARGS=(--register-unsafely-without-email)',
  '  echo "HTTPS_CERT_EMAIL ausente ou invalido. Emitindo certificado sem email de contato."',
  'fi',
  'mkdir -p .runtime/letsencrypt/conf .runtime/letsencrypt/www',
  'cp HANDOFF.md /opt/lavajato/runtime/HANDOFF_AWS.md',
  'cp AGENTS.md /opt/lavajato/runtime/AGENTS_AWS.md',
  'cp SKILLS.md /opt/lavajato/runtime/SKILLS_AWS.md',
  'export APP_BUILD_SHA="${DEPLOY_SHA}"',
  'export APP_HOST',
  'export APP_CERT_NAME',
  'docker compose build api web',
  'docker compose up -d --force-recreate api web',
  'for attempt in $(seq 1 18); do',
  '  if curl -fsS http://127.0.0.1/ | grep -F "app-build-sha" | grep -F "${DEPLOY_SHA}" > /dev/null; then',
  '    echo "Frontend serving deployed SHA ${DEPLOY_SHA} over HTTP."',
  '    break',
  '  fi',
  '  if [ "${attempt}" -eq 18 ]; then',
  '    echo "Frontend did not serve deployed SHA ${DEPLOY_SHA} over HTTP after deploy."',
  '    curl -fsS http://127.0.0.1/ || true',
  '    exit 1',
  '  fi',
  '  sleep 5',
  'done',
  'for attempt in $(seq 1 18); do',
  '  if curl -fsS http://127.0.0.1/api/health > /dev/null; then',
  '    echo "API respondeu health check em HTTP."',
  '    break',
  '  fi',
  '  if [ "${attempt}" -eq 18 ]; then',
  '    echo "API nao respondeu o health check em HTTP a tempo."',
  '    curl -fsS http://127.0.0.1/api/health || true',
  '    exit 1',
  '  fi',
  '  sleep 5',
  'done',
  'echo "Liberando espaco antes da etapa HTTPS..."',
  'docker system df || true',
  'docker container prune -f || true',
  'docker image prune -af || true',
  'docker builder prune -af || true',
  'docker system df || true',
  'docker compose pull certbot',
  'docker compose run --rm certbot --version',
  'docker compose run --rm certbot certonly --preferred-profile shortlived --webroot --webroot-path /var/www/certbot --non-interactive --agree-tos --keep-until-expiring --cert-name "${APP_CERT_NAME}" "${CERTBOT_CONTACT_ARGS[@]}" --ip-address "${APP_HOST}"',
  'docker compose exec -T web nginx -t',
  'docker compose exec -T web /usr/local/bin/render-nginx-config.sh',
  'docker compose exec -T web nginx -t',
  'docker compose exec -T web nginx -s reload',
  'install -m 755 infra/aws/renew-https.sh /usr/local/bin/lavajato-renew-https.sh',
  'cat <<SERVICE >/etc/systemd/system/lavajato-https-renew.service',
  '[Unit]',
  'Description=Renew LavaJato HTTPS certificate',
  '',
  '[Service]',
  'Type=oneshot',
  `Environment=HTTPS_CERT_EMAIL=${'${HTTPS_CERT_EMAIL}'}`,
  'ExecStart=/usr/local/bin/lavajato-renew-https.sh',
  'SERVICE',
  'cat <<TIMER >/etc/systemd/system/lavajato-https-renew.timer',
  '[Unit]',
  'Description=Run LavaJato HTTPS certificate renewal every 6 hours',
  '',
  '[Timer]',
  'OnBootSec=5min',
  'OnUnitActiveSec=6h',
  'Persistent=true',
  '',
  '[Install]',
  'WantedBy=timers.target',
  'TIMER',
  'systemctl daemon-reload',
  'systemctl enable --now lavajato-https-renew.timer',
  'systemctl list-timers --all | grep -F "lavajato-https-renew" || true',
  'for attempt in $(seq 1 18); do',
  '  if curl -fsS --connect-to "${APP_HOST}:443:127.0.0.1:443" "https://${APP_HOST}/" | grep -F "app-build-sha" | grep -F "${DEPLOY_SHA}" > /dev/null; then',
  '    echo "Frontend serving deployed SHA ${DEPLOY_SHA} over HTTPS."',
  '    break',
  '  fi',
  '  if [ "${attempt}" -eq 18 ]; then',
  '    echo "Frontend did not serve deployed SHA ${DEPLOY_SHA} over HTTPS after deploy."',
  '    curl -kfsS --connect-to "${APP_HOST}:443:127.0.0.1:443" "https://${APP_HOST}/" || true',
  '    exit 1',
  '  fi',
  '  sleep 5',
  'done',
  'docker compose ps',
  'curl -fsS --connect-to "${APP_HOST}:443:127.0.0.1:443" "https://${APP_HOST}/api/health"',
  'docker compose exec -T web rm -f /usr/share/nginx/html/deploy-debug.txt || true',
  'rm -f "${DEBUG_LOG}" || true',
].join('\n');

const commandPayload = {
  commands: [
    `cat <<'SCRIPT' >/tmp/lavajato-deploy.sh\n${deployScript}\nSCRIPT`,
    'bash /tmp/lavajato-deploy.sh',
  ],
};

process.stdout.write(JSON.stringify(commandPayload));
